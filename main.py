DEFAULT_PORT = 3000


def resolve_public_domain() -> str:
    domains = os.environ.get("REPLIT_DOMAINS", "").strip()
    if domains:
        first = domains.split(",")[0].strip()
        if first:
            return first
    dev_domain = os.environ.get("REPLIT_DEV_DOMAIN", "").strip()
    if dev_domain:
        return dev_domain
    raise RuntimeError(
        "Не удалось определить публичный домен. "
        "Должна быть установлена переменная REPLIT_DOMAINS (в продакшене) "
        "или REPLIT_DEV_DOMAIN (в разработке)."
    )


async def health_handler(request: web.Request) -> web.Response:
    return web.Response(text="OK")


def make_webhook_handler(application: "Application") -> "web.RequestHandler":
    async def handle(request: web.Request) -> web.Response:
        try:
            data = await request.json()
        except Exception:
            return web.Response(status=400, text="Bad Request")
        update = Update.de_json(data, application.bot)
        if update is not None:
            await application.update_queue.put(update)
        return web.Response(text="OK")

    return handle


async def run_bot() -> None:
    token = os.environ.get("TELEGRAM_BOT_TOKEN")
    if not token:
        raise RuntimeError(
            "TELEGRAM_BOT_TOKEN is not set. Add it as a Replit Secret."
        )

    public_domain = resolve_public_domain()
    port = int(os.environ.get("PORT", str(DEFAULT_PORT)))

    webhook_path = f"/webhook/{token}"
    webhook_url = f"https://{public_domain}{webhook_path}"

    application = ApplicationBuilder().token(token).build()

    application.add_handler(CommandHandler("start", start))
    application.add_handler(CommandHandler("help", help_command))
    application.add_handler(CommandHandler("protest", protest_command))
    application.add_handler(CommandHandler("birthday", birthday_command))
    application.add_handler(CommandHandler("antifreeze", antifreeze_command))
    application.add_handler(CommandHandler("all", all_command))
    application.add_handler(CallbackQueryHandler(menu_callback))
    application.add_error_handler(error_handler)

    web_app = web.Application()
    web_app.router.add_get("/", health_handler)
    web_app.router.add_post(webhook_path, make_webhook_handler(application))

    await application.initialize()
    await application.bot.set_webhook(
        url=webhook_url,
        allowed_updates=Update.ALL_TYPES,
        drop_pending_updates=True,
    )
    await application.start()

    runner = web.AppRunner(web_app, access_log=None)
    await runner.setup()
    site = web.TCPSite(runner, "0.0.0.0", port)
    await site.start()

    logger.info("HTTP server listening on 0.0.0.0:%d", port)
    logger.info("Telegram webhook set to %s", webhook_url)
    logger.info("Bot is running (webhook mode)...")

    stop_event = asyncio.Event()
    try:
        await stop_event.wait()
    except (KeyboardInterrupt, asyncio.CancelledError):
        pass
    finally:
        logger.info("Shutting down...")
        try:
            await application.bot.delete_webhook()
        except Exception as exc:
            logger.warning("Failed to delete webhook: %s", exc)
        try:
            await application.stop()
        except Exception as exc:
            logger.warning("Failed to stop application cleanly: %s", exc)
        try:
            await application.shutdown()
        except Exception as exc:
            logger.warning("Failed to shutdown application cleanly: %s", exc)
        await runner.cleanup()


def main() -> None:
    try:
        asyncio.run(run_bot())
    except KeyboardInterrupt:
        pass


if __name__ == "__main__":
    main()

