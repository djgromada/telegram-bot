USER_MODES: dict[int, str] = {}


def get_mode(chat_id: int) -> str:
    return USER_MODES.get(chat_id, DEFAULT_MODE)


def set_mode(chat_id: int, mode: str) -> None:
    if mode in ANTIFREEZE_POOLS:
        USER_MODES[chat_id] = mode


def format_mode_footer(mode: str) -> str:
    return f"\n\n[режим: {MODE_LABELS.get(mode, mode)}]"


def _people_word(n: int) -> str:
    if n == 1:
        return "1 человек"
    if 2 <= n <= 4:
        return f"{n} человека"
    return f"{n} человек"


def _format_tasks(tasks: list[str]) -> str:
    return "\n".join(f"• {t}" for t in tasks)


def _format_entrances(entrances: list[str]) -> str:
    return "\n".join(
        f"- {idx + 2}-й: {text}" for idx, text in enumerate(entrances)
    )


def build_protest_text(mode: str = DEFAULT_MODE) -> str:
    if mode not in PROTEST_POOLS:
        mode = DEFAULT_MODE
    topic = random.choice(PROTEST_POOLS[mode])
    return f"Сегодня мы протестуем против: {topic}.{format_mode_footer(mode)}"


def build_birthday_text(mode: str = DEFAULT_MODE) -> str:
    if mode not in GIFT_POOLS:
        mode = DEFAULT_MODE
    gift = random.choice(GIFT_POOLS[mode])
    return f"Странная идея подарка: {gift}.{format_mode_footer(mode)}"


